    private static final int RC_SIGN_IN = 1;
    FirebaseUser mUser;
    FirebaseAuth mAuth;


    @Override
    protected void onResume() {
        super.onResume();
        //add the listener in onResume.
        mAuth.addAuthStateListener(mAuthListener);
    }

    @Override
    protected void onPause() {
        super.onPause();
        //remove the listener.
        mAuth.removeAuthStateListener(mAuthListener);
    }


